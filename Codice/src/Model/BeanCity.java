package Model;

public class BeanCity
{
	private String cap;
	private String city;
	private String prov;
	
	public BeanCity()
	{
		
	}

	public String getCap()
	{
		return cap;
	}

	public String getCity()
	{
		return city;
	}

	public String getProv()
	{
		return prov;
	}

	public void setCap(String cap)
	{
		this.cap = cap;
	}

	public void setCity(String city)
	{
		this.city = city;
	}

	public void setProv(String prov)
	{
		this.prov = prov;
	}
	
}
